--TABELA DE CATEGORIAS
CREATE TABLE CATEGORIAS (
	ID_CATEGORIA INT PRIMARY KEY IDENTITY(1,1),
	NOME VARCHAR(100) NOT NULL,
	DESCRICAO VARCHAR(255)
);

--TABELA FORNECEDORES
CREATE TABLE FORNECEDORES (
	ID_FORNECEDOR INT PRIMARY KEY IDENTITY(1,1),
	NOME VARCHAR(150) NOT NULL,
	CONTATO VARCHAR(100),
	TELEFONE VARCHAR(20)
);

--TABELA PRODUTOS
CREATE TABLE PRODUTOS (
	ID_PRODUTO INT PRIMARY KEY IDENTITY(1,1),
	NOME VARCHAR(150) NOT NULL,
	ID_CATEGORIA INT FOREIGN KEY REFERENCES CATEGORIAS(ID_CATEGORIA),
	ID_FORNECEDOR INT FOREIGN KEY REFERENCES FORNECEDORES(ID_FORNECEDOR),
	PRECO_UNITARIO DECIMAL(10,2) NOT NULL,
	QUANTIDADE_ESTOQUE INT DEFAULT 0,
	ESTOQUE_MINIMO INT DEFAULT 5
);

--TABELA DE MOVIMENTAÇĂO (ENTRADA E SAÍDA)
CREATE TABLE MOVIMENTACOES (
	ID_MOVIMENTACAO INT PRIMARY KEY IDENTITY(1,1),
	ID_PRODUTO INT FOREIGN KEY REFERENCES PRODUTOS(ID_PRODUTO),
	TIPO VARCHAR(10) CHECK (TIPO IN ('ENTRADA', 'SAIDA')) NOT NULL,
	QUANTIDADE INT NOT NULL,
	DATA_MOVIMENTACAO DATETIME DEFAULT GETDATE(),
	OBSERVACAO VARCHAR(255)
);

