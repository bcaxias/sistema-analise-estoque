--Ver todos os produtos com categorias e fornecedor
SELECT 
	P.NOME AS PRODUTO,
	C.NOME AS CATEGORIA,
	F.NOME AS FORNECEDOR,
	P.PRECO_UNITARIO AS PREÇO,
	P.QUANTIDADE_ESTOQUE AS ESTOQUE,
	P.ESTOQUE_MINIMO AS MÍNIMO
FROM PRODUTOS P
INNER JOIN CATEGORIAS C ON P.ID_CATEGORIA = C.ID_CATEGORIA
INNER JOIN FORNECEDORES F ON P.ID_FORNECEDOR = F.ID_FORNECEDOR;

-- Ver produtos com estoque abaixo do mínimo
SELECT NOME, QUANTIDADE_ESTOQUE, ESTOQUE_MINIMO
FROM PRODUTOS
WHERE QUANTIDADE_ESTOQUE <= ESTOQUE_MINIMO;

-- Ver todas as movimentaçőes
SELECT 
	P.NOME AS PRODUTO,
    M.TIPO,
    M.QUANTIDADE,
    M.DATA_MOVIMENTACAO,
    M.OBSERVACAO
FROM MOVIMENTACOES M
INNER JOIN PRODUTOS P ON M.ID_PRODUTO = P.ID_PRODUTO
ORDER BY M.DATA_MOVIMENTACAO DESC;

